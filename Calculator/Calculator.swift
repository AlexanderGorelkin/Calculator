import Foundation

class Calculator {
    func addition(first: Float, second: Float) -> Float{
        first + second
    }
    func subtraction(first: Float, second: Float) -> Float{
        first - second
    }
    func multiplication(first: Float, second: Float) -> Float{
        first * second
    }
    func division(first: Float, second: Float) -> Float{
        first / second
    }
}
